        <p><span style="color: #000080;"><b>What is COVID-19?</b></span></p>
        <p>COVID-19 is a disease caused by a new strain of coronavirus. ‘CO’ stands for corona, ‘VI’ for virus, and ‘D’ for disease.</p>
        <p>Formerly, this disease was referred to as ‘2019 novel coronavirus’ or ‘2019-nCoV.’ The COVID-19 virus is a new virus linked to the same family of viruses as Severe Acute Respiratory Syndrome (SARS) and some types of common cold.</p>
        <p><span style="color: #000080;"><b>What are the symptoms of COVID-19?</b> </span></p>
        <p>Fever, cough and shortness of breath. In more severe cases, infection can cause pneumonia or breathing difficulties. More rarely, the disease can be fatal. These symptoms are similar to the flu (influenza) or the common cold, which are a lot more common than COVID-19. This is why testing is required to confirm if someone has COVID-19.</p>
        <p><span style="color: #000080;"><b>How does COVID-19 spread</b></span></p>
        <p>The virus is transmitted through direct contact with respiratory droplets of an infected person (generated through coughing and sneezing). Individuals can also be infected from and touching surfaces contaminated with the virus and touching their face (e.g., eyes, nose, mouth). The COVID-19 virus may survive on surfaces for several hours, but simple disinfectants can kill it.</p>
        <p><span style="color: #000080;"><b>Who is most at risk?</b></span></p>
        <p>Older people, and people with chronic medical conditions, such as diabetes and heart disease.  Few cases of COVID-19 reported among children. The virus can be fatal in rare cases, so far mainly among older people with pre-existing medical conditions.</p>
        <p><span style="color: #000080;"><b>What is the treatment for COVID-19?</b> </span></p>
        <p>Many of the symptoms can be treated. Getting early care from a healthcare provider can make the disease less dangerous. There are several clinical trials that are being conducted to evaluate potential therapeutics for COVID-19.</p>
        <p><span style="color: #000080;"><b>How can the spread of COVID-19 be slowed down or prevented?</b></span></p>
        <p>staying home when sick;</p>
        <p>covering mouth and nose with flexed elbow or tissue when coughing or sneezing.</p>
        <p>Dispose of used tissue immediately;</p>
        <p>washing hands often with soap and water;</p>
        <p>cleaning frequently touched surfaces and objects.</p>
        <p><strong><span style="color: #000080;">Home care for patients with suspected COVID-19 infection who present with mild symptoms</span></strong></p>
        <p>Educate patients and household members on personal hygiene, basic Infection Prevention &amp;Control measures.</p>
        <p>Place the patient in a well-ventilated single room (i.e., with open windows and an open door).</p>
        <p>Limit the movement of the patient in the house and minimize shared space.</p>
        <p>Household members should stay in a different room or, maintain a distance of at least 1 m from the ill person (e.g., sleep in a separate bed)</p>
        <p>Limit the number of caregivers. Ideally, assign one person who is in a good health and has no underlying chronic or immuno compromising conditions</p>
        <p>Perform hand hygiene after any type of contact with patients or their immediate environment</p>
        <p>When washing hands with soap and water, it is preferable to use disposable paper towels to dry hands</p>
        <p>A medical mask should be provided to the patient and worn as much as possible</p>
        <p>Caregivers should wear a tightly fitted medical mask that covers their mouth and nose when in the same room as the patient. Masks should not be touched or handled during use</p>
        <p>Avoid direct contact with body fluids, particularly oral or respiratory secretions, and stool</p>
        <p>Do not reuse masks or gloves</p>
        <p>Use dedicated linen and eating utensils for the patient; these items should be cleaned with soap and water after use and may be re-used instead of being discarded</p>
        <p>Clean and disinfect daily surfaces that are frequently touched in the room where the patient is being cared for, such as bedside tables, bedframes and other bedroom furniture</p>
        <p>Avoid other types of exposure to contaminated items from the patient’s immediate environment (e.g., do not share toothbrushes, cigarettes, eating utensils, dishes, drinks, towels, washcloths or bed linen).</p>
        <p><strong><span style="color: #000080;">A contact is a person who has had any of the following exposures:</span></strong></p>
        <p>A healthcare-associated exposure, including providing direct care for patients with COVID-19 disease</p>
        <p>An exposure through working together in close proximity with a patient with COVID-19 disease</p>
        <p>An exposure through traveling with a patient who has COVID-19 disease in any kind of vehicle;</p>
        <p>An exposure through living in the same household as a patient with COVID-19 disease within 14 days after the onset of symptoms in the patient</p>
        <p><strong><span style="color: #000080;">If a contact develops symptoms, the following steps should be taken:</span></strong></p>
        <p>Notify the receiving medical facility that a symptomatic contact will be arriving</p>
